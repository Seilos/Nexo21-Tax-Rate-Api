def parse_and_seed():
    print("Analizando y cargando datos reales del usuario...")
    lines = raw_data.strip().split('\n')
    
    total_created = 0
    for line in lines:
        parts = line.split('\t')
        if len(parts) < 4:
            continue
            
        try:
            # Fecha Valor BCV está en la columna 1 (según el pegado del usuario)
            fecha_str = parts[1].strip()
            usd_str = parts[2].strip().replace(',', '.')
            eur_str = parts[3].strip().replace(',', '.')
            
            # Formato de fecha: DD/MM/YYYY
            fecha_dt = datetime.strptime(fecha_str, "%d/%m/%Y").date()
            
            usd_val = Decimal(usd_str)
            eur_val = Decimal(eur_str)
            
            # Crear o actualizar USD
            obj_usd, created_usd = ExchangeRate.objects.update_or_create(
                source="BCV", currency="USD", fecha_valor=fecha_dt,
                defaults={'value': usd_val, 'source_url': 'https://www.bcv.org.ve/'}
            )
            if created_usd: total_created += 1
            
            # Crear o actualizar EUR
            obj_eur, created_eur = ExchangeRate.objects.update_or_create(
                source="BCV", currency="EUR", fecha_valor=fecha_dt,
                defaults={'value': eur_val, 'source_url': 'https://www.bcv.org.ve/'}
            )
            if created_eur: total_created += 1
            
        except Exception as e:
            print(f"Error procesando línea: {line}. Error: {e}")
            
    print(f"Éxito: Se procesaron {len(lines)} registros.")
    print(f"Se crearon/actualizaron {total_created} nuevas entradas de alta precisión.")

if __name__ == "__main__":
    parse_and_seed()
